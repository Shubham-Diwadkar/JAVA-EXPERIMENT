// Write a program to illustrate for loop in java

public class For_Loop
{
    public static void main(String[] args)
    {
        int i;                                          // Declare the variable i.
        for(i = 0; i < 5; i++)
        {
            System.out.println("Value of i: " + i);     // Print the value of i.
        }
    }
}
