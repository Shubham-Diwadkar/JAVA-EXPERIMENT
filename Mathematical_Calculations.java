public class Mathematical_Calculations {
    public static void main(String[] args) {
        double A = 10;                          // Assigning the value 10 to the variable A
        double B = 4;                           // Assigning the value 4 to the variable B
        
        System.out.println(A + B);              // Printing the sum of A and B
        System.out.println(A - B);              // Printing the difference of A and B
        System.out.println(A * B);              // Printing the product of A and B
        System.out.println(A / B);              // Printing the quotient of A and B
    }
}
