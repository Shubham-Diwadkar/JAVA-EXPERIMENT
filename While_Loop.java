// Write a program to illustrate while loop in Java.

class While_Loop {
	public static void main(String args[])
	{
		int i = 1;                                      // initialization expression
		while (i < 6)                                   // test expression
        {
			System.out.println("Value of i: " + i);     // printing value of i
			i++;                                        // update expression
		}
	}
}