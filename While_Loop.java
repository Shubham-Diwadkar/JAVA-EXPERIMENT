// Write a program to illustrate while loop in Java.

class While_Loop
{
    public static void main(String[] args)
    {
        int i = 1;                                      // Declaring and initializing the variable i with the value 1.
        while(i<6)                                      // Checking if the value of i is less than 6.
        {
            System.out.println("Value of i: " + i);     // Printing the value of i.
            i++;                                        // Incrementing the value of i by 1.
        }    
    }
}
