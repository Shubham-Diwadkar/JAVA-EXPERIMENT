// Write a program to demonstrate if program.

class If_Program
{
    public static void main(String[] args)
    {
        int x = 10;                             // Declaring and initializing the variable x with the value 10.
        
        // Checking if the value of x is equal to 10.
        if(x == 10)
        {
            int y = 20;                         // Declaring and initializing the variable y with the value 20.
            System.out.println("x = "+x);       // Printing the value of x.
            System.out.println("y = "+y);       // Printing the value of y.
        }
    }
}
